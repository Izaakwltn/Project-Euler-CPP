#include <iostream>

int main()
{
	int i = 1;
	int j = 1;
	int k;
	int fibsum;
	while (i < 4000000)
		{   
			if (i % 2 == 0)
			{
				fibsum += i;
			} 
			
			k = j;
			j = i;
			i = k + i;
		}
	std::cout << fibsum << "\n";
}